  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_TIM2_Init();
  MX_TIM22_Init();

  /* USER CODE BEGIN 2 */
  /* Compute the prescaler value to have TIM2 counter clock equal to 2000000 Hz */
   uhPrescalerValue = (uint32_t)(SystemCoreClock / 2000000) - 1;


